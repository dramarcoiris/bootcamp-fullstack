package exercise01;

public class Curso {
   String titulo;
   int duracionHoras;
   double precio;
   boolean activo;

   public Curso(String titulo, int duracionHoras, double precio, boolean activo) {
      this.titulo = titulo;
      this.duracionHoras = duracionHoras;
      this.precio = precio;
      this.activo = activo;
   }
}
